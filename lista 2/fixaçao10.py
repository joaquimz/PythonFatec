week=int(input("Insira o numero da semana (1-7): "))
if week==1:
    print("Segunda-Feira")
elif week==2:
    print("Terça-Feira")
elif week==3:
    print("Quarta-Feira")
elif week==4:
    print("Quinta-Feira")
elif week==5:
    print("Sexta-Feira")
elif week==6:
    print("Sabado")
elif week==7:
    print("Domingo")
else:
    print("Numero da semana invalido!")
